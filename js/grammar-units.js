// ==================== HELPERS ====================

// Get the unit by id
function getGrammarUnit(unitId) {
    return GRAMMAR_UNITS.find(u => u.id === unitId);
}

// Generate a quiz of N random questions from a unit
function generateGrammarQuiz(unitId, n) {
    const unit = getGrammarUnit(unitId);
    if (!unit) return [];
    const pool = [...unit.questions];
    // Fisher-Yates shuffle
    for (let i = pool.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [pool[i], pool[j]] = [pool[j], pool[i]];
    }
    return pool.slice(0, Math.min(n, pool.length));
}

// Stats per unit (best score, attempts, last attempt)
function getGrammarStats(unitId) {
    if (!appState || !appState.grammarHistory) return { attempts: 0, best: null, last: null };
    const sessions = appState.grammarHistory.filter(s => s.unitId === unitId);
    if (sessions.length === 0) return { attempts: 0, best: null, last: null };
    const sorted = [...sessions].sort((a, b) => b.date - a.date);
    const best = sessions.reduce((acc, s) => (!acc || s.score > acc.score) ? s : acc, null);
    return { attempts: sessions.length, best, last: sorted[0] };
}

// Check if an arrangement answer is correct
function isArrangementCorrect(userAnswer, expectedLength) {
    if (!Array.isArray(userAnswer)) return false;
    if (userAnswer.length !== expectedLength) return false;
    return userAnswer.every((idx, pos) => idx === pos);
}

// Score one question
function scoreGrammarQuestion(q, userAnswer) {
    if (q.type === 'arrangement') {
        return isArrangementCorrect(userAnswer, q.parts.length) ? 1 : 0;
    }
    return userAnswer === q.correct ? 1 : 0;
}

// Save a completed quiz session to history
function saveGrammarSession(unitId, questions, answers) {
    if (!appState) return;
    if (!appState.grammarHistory) appState.grammarHistory = [];
    const correctCount = questions.reduce((sum, q, i) => sum + scoreGrammarQuestion(q, answers[i]), 0);
    const session = {
        id: 'g-' + Date.now(),
        unitId,
        date: Date.now(),
        score: correctCount,
        total: questions.length,
        questions: questions.map((q, i) => ({
            id: q.id,
            type: q.type,
            topic: q.topic,
            q: q.q,
            options: q.options,           // undefined for arrangement
            correct: q.correct,           // undefined for arrangement
            parts: q.parts,                // undefined for non-arrangement
            explanation: q.explanation,
            userAnswer: answers[i]
        }))
    };
    appState.grammarHistory.unshift(session); // Newest first
    // Cap history at 50 sessions
    if (appState.grammarHistory.length > 50) appState.grammarHistory = appState.grammarHistory.slice(0, 50);
    saveUserData(currentUser, appState);
    return session;
}
